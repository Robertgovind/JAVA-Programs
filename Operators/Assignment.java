package Operators;

public class Assignment {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a += 5); // a=a+5
        System.out.println(a -= 5); // a=a-5
        System.out.println(a *= 4); // a=a*4
        System.out.println(a /= 5); // a=a/5
        System.out.println(a %= 3); // a=a%3
    }
}
